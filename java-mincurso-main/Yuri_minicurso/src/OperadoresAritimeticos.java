
public class OperadoresAritimeticos {

	public static void main(String[] args) {
	int num1 = 5;
	int num2 = 2;
	
	// Operadores de incremento
	int i = 1;
	i++; //i = i+i
	System.out.println("incremento "+i);
	
	//Decrementar
	int x = 2;
	x--; //x=x_1
	System.out.println("decremento "+x);
	
	//
	
	System.out.println("operação de adição: "+(num1+num2));
	System.out.println("operação de subtração: "+(num1-num2));
	System.out.println("operação de multipicação: "+(num1*num2));
	System.out.println("operação de divisão:: "+(num1/num2));
	System.out.println("operação de resto: "+(num1%num2));
	
	
	
	
	
	
	
	
	}
	
}